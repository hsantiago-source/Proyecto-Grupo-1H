    private void BotonDatosRedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonDatosRedActionPerformed

        if (redCargadaEnMemoria) {
            JOptionPane.showMessageDialog(this, 
                "Alerta: Ya hay una red  cargada en memoria.\nRecuerda guardar tus datos actuales si hiciste modificaciones.", 
                "Advertencia de Sobreescritura", 
                JOptionPane.WARNING_MESSAGE);
        }

        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(".CSV","csv");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        fileChooser.setFileFilter(filter);
        fileChooser.setDialogTitle("Seleccione el archivo CSV de la Red Neuronal");
        
        int seleccion = fileChooser.showOpenDialog(this);
        
        if (seleccion == JFileChooser.APPROVE_OPTION) {

            File archivo = fileChooser.getSelectedFile();
            String ruta = archivo.getAbsolutePath(); 
            
            try {
                BufferedReader lector = new BufferedReader(new FileReader(archivo));
                String linea;
                
                
                Logica.LectorCSV lectorLogico = new Logica.LectorCSV();
                Boolean aprobado = lectorLogico.leerRedNeuronal(ruta);
               
                if (aprobado == true) {
                    String contenidoCompleto = ""; 
                    
                    while ((linea = lector.readLine()) != null) {
                        contenidoCompleto += linea + "\n";
                    }
                    lector.close();
                    
                    AreaTextoRed.setText(contenidoCompleto);

                
                    redCargadaEnMemoria = true;
                }
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al leer el archivo: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_BotonDatosRedActionPerformed

