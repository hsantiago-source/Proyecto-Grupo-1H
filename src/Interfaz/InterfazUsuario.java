    private void RedGrafoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RedGrafoActionPerformed
        
        GrafoSinaptico grafoGlobal = ControladorPrincipal.getGrafo();

        if (grafoGlobal.isEmpty()) {
            JOptionPane.showMessageDialog(this, "La red neuronal está vacía. Cargue el CSV primero.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        System.setProperty("org.graphstream.ui.renderer", "org.graphstream.ui.j2dviewer.J2DGraphRenderer");
        Graph graph = new SingleGraph("Cerebro");
        graph.setStrict(false);
        graph.setAutoCreate(true);

        Neurona neuronaActual = grafoGlobal.getNeuronaInicio();
        while (neuronaActual != null) {
            if (neuronaActual.getActivo()) {
                Node n = graph.addNode(neuronaActual.getId());
                n.setAttribute("ui.label", neuronaActual.getId()); 
            }
            neuronaActual = neuronaActual.getSiguiente();
        }

        neuronaActual = grafoGlobal.getNeuronaInicio();
        while (neuronaActual != null) {
                Sinapsis sinapsisActual = neuronaActual.getConexiones().getPrimeraSinapsis();
                
                while (sinapsisActual != null) {
                    if (sinapsisActual.getDestino().getActivo()) {
                        String idOrigen = neuronaActual.getId();
                        String idDestino = sinapsisActual.getDestino().getId();
                        String idArista = idOrigen + "-" + idDestino;

                        graph.addEdge(idArista, idOrigen, idDestino, true);
                    }
                    sinapsisActual = sinapsisActual.getSiguiente();
                }
            
            neuronaActual = neuronaActual.getSiguiente();
        }
        
            String styleSheet = "node {" +
                            "   fill-color: green;" +
                            "   size: 30px;" +
                            "   text-size: 24px;" +         
                            "   text-style: bold;" +        
                            "   text-alignment: center;" +
                            "}" +
                            "edge {" +
                            "   fill-color: black;" +
                            "}";
        graph.setAttribute("ui.stylesheet", styleSheet);

        Viewer viewer = new Viewer(graph, Viewer.ThreadingModel.GRAPH_IN_ANOTHER_THREAD);
        viewer.enableAutoLayout(); 
        
        ViewPanel viewPanel = viewer.addDefaultView(false); 

        muestraGrafo.removeAll();
        muestraGrafo.setLayout(new BorderLayout());
        muestraGrafo.add(viewPanel, BorderLayout.CENTER);
        
        PanelContenedor.removeAll();
        PanelContenedor.add(VistaVerRed);
        PanelContenedor.repaint();
        PanelContenedor.revalidate();
    }//GEN-LAST:event_RedGrafoActionPerformed

