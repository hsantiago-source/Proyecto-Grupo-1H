    private void EliminarNeuronaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarNeuronaActionPerformed
    
    String idIngresado = IdNeurona.getText().trim();
        
        if (idIngresado.isEmpty() == true) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el ID de la neurona a eliminar.", "Campo Vacío", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        GrafoSinaptico grafoGlobal = ControladorPrincipal.getGrafo();
        
        if (grafoGlobal.existeNeurona(idIngresado) == false) {
            JOptionPane.showMessageDialog(this, "Error: La neurona '" + idIngresado + "' no existe en la red.", "Error de Eliminación", JOptionPane.ERROR_MESSAGE);
        } else {
            grafoGlobal.eliminarNeurona(idIngresado); 
            JOptionPane.showMessageDialog(this, "Neurona '" + idIngresado + "' y todas sus conexiones fueron eliminadas permanentemente.");
        }
        
        IdNeurona.setText("");
        
        if (grafoGlobal != null) {
            TextAreaNeurona.setText(grafoGlobal.mostrarListaNeuronas());
        }
    }//GEN-LAST:event_EliminarNeuronaActionPerformed

    private void AgregarNeuronaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarNeuronaActionPerformed
    
    String idIngresado = IdNeurona.getText().trim();
        
        if (idIngresado.isEmpty() == true) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un ID válido.", "Campo Vacío", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        GrafoSinaptico grafoGlobal = ControladorPrincipal.getGrafo();
        
        if (grafoGlobal.existeNeurona(idIngresado) == true) {
            JOptionPane.showMessageDialog(this, "La neurona '" + idIngresado + "' ya existe en la red.", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            grafoGlobal.agregarNeurona(idIngresado);
            JOptionPane.showMessageDialog(this, "Neurona '" + idIngresado + "' agregada exitosamente.");
        }
        
        IdNeurona.setText("");
        
        if (grafoGlobal != null) {
            TextAreaNeurona.setText(grafoGlobal.mostrarListaNeuronas());
        }
    }//GEN-LAST:event_AgregarNeuronaActionPerformed

